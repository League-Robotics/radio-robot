"""Off-hardware acceptance proof for ticket 095-005 (SUC-004, M4 "Generated
Envelope Codec").

Compiles ``wire_codec_harness.cpp`` (exercising ``msg::wire::decode()``/
``encode()`` in ``src/firm/messages/wire.{h,cpp}`` -- GENERATED by
``scripts/gen_messages.py`` from ``protos/envelope.proto`` et al. -- against
every implemented ``CommandEnvelope``/``ReplyEnvelope`` arm plus a
representative declared-only arm) with the system C++ compiler, runs the
resulting binary, and asserts it exits 0. Mirrors
``test_wire_runtime.py``'s exact shape for the normal build.

A second test recompiles the SAME harness with
``-fsanitize=address,undefined`` and reruns every scenario, proving the
generated offsetof-based field-table walker never reads/writes out of
bounds -- in particular the repeated-message max_count clamp (``out[]``
sized to exactly ``max_count`` in one scenario) and every malformed/
boundary-rejection scenario.

This is DISTINCT from ticket 006's differential-against-``google.protobuf``
suite (not yet written this sprint) -- this harness checks the generated
engine's OWN behavior in isolation (round-trip, missing-``req`` reject,
out-of-``min``/``max``/``abs_max`` reject, unknown-field skip, oversized-
buffer ``encode()`` rejection, repeated-field ``max_count`` clamp), not
byte-for-byte agreement with a reference protobuf implementation.

Collected under ``src/tests/sim/unit/`` -- already within ``pyproject.toml``'s
``testpaths = ["src/tests/sim"]``, no configuration change needed.
"""

import pathlib
import subprocess
import sys

import pytest

# src/tests/sim/unit/test_wire_codec.py -> unit -> sim -> tests -> repo root
_REPO_ROOT = pathlib.Path(__file__).resolve().parents[4]
_SOURCE_DIR = _REPO_ROOT / "src" / "firm"
_HARNESS_SRC = pathlib.Path(__file__).resolve().parent / "wire_codec_harness.cpp"
_WIRE_SRC = _SOURCE_DIR / "messages" / "wire.cpp"
_WIRE_RUNTIME_SRC = _SOURCE_DIR / "messages" / "wire_runtime.cpp"

# wire.h/wire_runtime.h both document the project's ACTUAL compiled standard
# as -std=gnu++20 (095-003's finding) -- build the host harness to the same
# standard.
_CXX_STANDARD = "c++20"


def _find_cxx_compiler() -> str:
    """Locate a usable system C++ compiler, preferring c++ then clang++/g++."""
    import shutil

    for candidate in ("c++", "clang++", "g++"):
        found = shutil.which(candidate)
        if found:
            return found
    pytest.skip("no system C++ compiler (c++/clang++/g++) found on PATH")
    raise AssertionError("unreachable")  # pragma: no cover


def _compile_and_run(tmp_path, binary_name: str, extra_flags: list[str]) -> None:
    assert _HARNESS_SRC.is_file(), f"harness source missing: {_HARNESS_SRC}"
    assert _WIRE_SRC.is_file(), f"wire.cpp missing (run scripts/gen_messages.py?): {_WIRE_SRC}"
    assert _WIRE_RUNTIME_SRC.is_file(), f"wire_runtime.cpp missing: {_WIRE_RUNTIME_SRC}"
    assert _SOURCE_DIR.is_dir(), f"src/firm/ tree missing: {_SOURCE_DIR}"

    cxx = _find_cxx_compiler()
    binary = tmp_path / binary_name

    compile_result = subprocess.run(
        [
            cxx,
            f"-std={_CXX_STANDARD}",
            "-Wall",
            "-Wextra",
            *extra_flags,
            "-I",
            str(_SOURCE_DIR),
            "-o",
            str(binary),
            str(_HARNESS_SRC),
            str(_WIRE_SRC),
            str(_WIRE_RUNTIME_SRC),
        ],
        capture_output=True,
        text=True,
    )
    assert compile_result.returncode == 0, (
        "wire_codec_harness.cpp failed to compile:\n"
        f"stdout:\n{compile_result.stdout}\nstderr:\n{compile_result.stderr}"
    )

    run_result = subprocess.run([str(binary)], capture_output=True, text=True)
    assert run_result.returncode == 0, (
        "wire_codec_harness reported a scenario failure "
        f"(exit {run_result.returncode}):\n{run_result.stdout}\n{run_result.stderr}"
    )


def test_wire_codec_harness_compiles_and_passes(tmp_path):
    """Compile the wire codec harness (normal host flags) and assert every scenario passes."""
    _compile_and_run(tmp_path, "wire_codec_harness", [])


def test_wire_codec_harness_asan_ubsan(tmp_path):
    """Recompile under ASan/UBSan and rerun every scenario -- proves the generated
    offsetof-based field-table walker (decodeInto/encodeInto in wire.cpp) never
    reads/writes out of bounds, not just that it returns the right values."""
    _compile_and_run(
        tmp_path,
        "wire_codec_harness_asan",
        ["-fsanitize=address,undefined", "-fno-omit-frame-pointer", "-g"],
    )


if __name__ == "__main__":
    sys.exit(pytest.main([__file__, "-v"]))
