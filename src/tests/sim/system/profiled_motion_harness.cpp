// profiled_motion_harness.cpp -- 106-006 (SUC-030) Phase 1: sim-validated
// proof that a REAL planner/profile.py-generated setpoint sequence, played
// into TestSim::SimHarness (migrated from the deleted TestSim::SimApi,
// ticket 108-004) one setpoint per cycle, produces the expected trapezoidal
// ramp shape (monotonic-ish accel, a held cruise plateau, monotonic-ish
// decel, converged-to-zero terminal) in the REAL plant + firmware's own
// decoded telemetry, for BOTH a profiled straight leg and a profiled
// in-place turn.
//
// --- Scope decision (ticket 006's own "document the choice in Completion
// Notes" instruction) ---
// This harness does NOT drive the REAL planner/executor.py StreamingExecutor
// against SimApi -- ticket 006's own Implementation Plan explicitly
// anticipated this gap ("architecture-update.md (105) Decision 4 explicitly
// deferred the Python sim ctypes wrapper to sprint 107, so this ticket likely
// instead injects the SAME setpoint sequence planner/profile.py would generate
// directly into SimApi.injectTwist() calls... without requiring a full
// Python-to-sim transport this sprint") and left the exact wiring to the
// implementer. That path is taken here: the setpoint SEQUENCE (elapsed,
// v_x, omega triples) is generated by the REAL, unmodified
// `robot_radio.planner.profile.profile_for_distance()`/`profile_for_turn()`
// (see the sibling `test_profiled_motion_sim.py`, which imports and calls
// them directly -- no reimplemented trapezoid math anywhere in this file or
// its Python driver) and written to a small CSV this harness reads. This
// harness's own job is pure glue: replay each row as one
// `SimApi::injectTwist()` call, one sim cycle (`kCycleDtUs` == the
// profile's own default 50ms cadence) per row -- it contains NO ramp-shape
// math and NO heading-correction/pacing logic of its own, so it is not a
// "reimplemented test-only model" of either `profile.py` (values come from
// the real generator) or `executor.py` (this harness makes no attempt to
// reproduce its pacing/heading-trim/overshoot-bound behavior at all).
// `executor.py`'s own binding-requirement logic already has real-code
// coverage against a fake transport in `src/tests/unit/test_planner_executor.py`
// (106-005) -- this file adds the complementary REAL-PLANT proof that
// `profile.py`'s own generated sequence, played open-loop into the real
// firmware, produces a genuine trapezoidal plant response, not just a
// mathematically correct setpoint list. A true StreamingExecutor-vs-SimApi
// bridge (e.g. over a persistent pipe, or a Python sim ctypes wrapper
// rebuilt against this tree's ABI) is future work for whichever ticket
// revives that wrapper (105's Decision 4 already scoped that to sprint
// 107; landed as `host/robot_radio/io/sim_loop.py`, ticket 108-006).
//
// --- Why v_x/omega_max here are REACHABLE (not saturating) ---
// Every OTHER scenario in this codebase deliberately commands a target far
// above `TestSim::kDefaultDutyVelMax` so `SimApi::DutyPredictor`'s simple
// saturated-PID prediction stays valid the whole run (`sim_api.h`'s own
// "Plant/PID tuning" section). This scenario needs the OPPOSITE: a
// REACHABLE cruise velocity, so the plant's closed loop actually leaves
// saturation and traces a real accelerate/cruise/decelerate shape -- exactly
// the case 106-003's `DutyPredictor` generalization was built to handle (see
// `sim_api.h`'s own comment: "A scenario that lets the target be REACHABLE
// ... and any future profile deceleration ... 106-003's own decay-to-zero
// scenario"). The straight leg's v_max/turn leg's omega_max (passed in via
// the CSV's own peak value) are chosen by the Python driver to sit
// comfortably under the plant's ceiling.
