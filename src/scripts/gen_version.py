#!/usr/bin/env python3
"""Generate src/firm/types/version_generated.h from the canonical project version.

The firmware reports its version over the wire (VER / ID replies read
FIRMWARE_VERSION from src/firm/types/protocol.h). That constant used to be
hand-edited and silently drifted -- the compiled-in string sat at an old
version (0.20260704.6) while pyproject.toml advanced past it, so VER lied about
which build was live. This codegen writes the version into a generated header
that protocol.h includes, sourced from the single canonical version in
pyproject.toml, so the wire version always matches the build.

Runs as part of build.py's codegen sequence, alongside gen_messages.py. The
generated header is git-ignored; protocol.h falls back to "0.0.0-dev" (via
__has_include) when it is absent, so clangd and codegen-less compiles still
build.
"""
import os
import re

ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
OUT = os.path.join(ROOT, "src", "firm", "types", "version_generated.h")


def project_version():
    """Read the canonical project version from the root pyproject.toml."""
    with open(os.path.join(ROOT, "pyproject.toml")) as f:
        for line in f:
            m = re.match(r'\s*version\s*=\s*"([^"]+)"', line)
            if m:
                return m.group(1)
    return "0.0.0-unknown"


def main():
    content = (
        "// GENERATED by scripts/gen_version.py -- do not edit, do not commit.\n"
        "// Firmware version string, sourced from pyproject.toml at build time.\n"
        "#pragma once\n"
        f'#define FIRMWARE_VERSION_STR "{project_version()}"\n'
    )
    # Only rewrite when the content actually changes, to avoid needless rebuilds.
    try:
        with open(OUT) as f:
            if f.read() == content:
                return
    except FileNotFoundError:
        pass
    with open(OUT, "w") as f:
        f.write(content)


if __name__ == "__main__":
    main()
