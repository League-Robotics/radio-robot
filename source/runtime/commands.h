// bitOf() -- the one place a *ConfigField enumerator becomes a `mask` bit.
// A caller building a ConfigDelta (or a fold routine reading one) always
// goes through these, never a raw shift, so the "bit i means field i of the
// enum matching `target`" invariant documented on ConfigDelta above cannot
// drift out of sync with the enums' own declaration order.
constexpr uint64_t bitOf(DrivetrainConfigField field) {
  return uint64_t{1} << static_cast<uint8_t>(field);
}
constexpr uint64_t bitOf(MotorConfigField field) {
  return uint64_t{1} << static_cast<uint8_t>(field);
}
constexpr uint64_t bitOf(PlannerConfigField field) {
  return uint64_t{1} << static_cast<uint8_t>(field);
}
constexpr uint64_t bitOf(OdometerConfigField field) {
  return uint64_t{1} << static_cast<uint8_t>(field);
}

