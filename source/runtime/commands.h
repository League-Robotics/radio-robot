// commands.h -- Rt::PoseResetCommand / Rt::ConfigDelta: lightweight,
// host-safe POD command-plane payload types shared between
// source/runtime/blackboard.h and the subsystems that drain them directly
// (Subsystems::PoseEstimator's poseResetIn parameter, ticket 087-004).
//
// Extracted OUT of blackboard.h (which is where architecture-update-r1.md's
// Reference code originally defined both structs): the boundary rule
// "subsystems never include blackboard.h" (Step 3 / Self-Review of that
// document) means PoseEstimator::tick()'s new
// Rt::WorkQueue<Rt::PoseResetCommand,4>& parameter cannot be typed by naming
// a struct defined INSIDE blackboard.h -- pose_estimator.h would have to
// include blackboard.h to see it, which is exactly the direction the
// boundary rule forbids (subsystems depend on Blackboard's queue/state
// TYPES only, never the aggregate itself -- see that document's Step 4
// dependency graph). Moved here, a lightweight, CODAL-free header both
// blackboard.h and pose_estimator.h can include without either including
// the other -- the SAME pattern Decision 10 (ticket 087-002) already
// established for Subsystems::CommunicatorToCommandProcessorStatement
// (source/subsystems/statement.h).
//
// Zero CODAL includes: <cstdint> plus messages/drivetrain.h (msg::SetPose,
// itself auto-generated by scripts/gen_messages.py with zero CODAL deps) --
// so any host test harness that includes this header (or transitively,
// blackboard.h/pose_estimator.h) never drags in MicroBit.h. Mechanical
// application of Decision 10's "every blackboard payload is a host-safe POD
// in a lightweight header" standing rule to these two types.
